package variable;

public class VariableType {
    public static void main(String[] args) {
        //변수타입 변수명;
        int one = 1;
        System.out.println(one);
        //정수만 들어가는 박스에 one라는 이름표를 붙임
        //변수명 = 넣고 싶은 값;
        one = 50;
        System.out.println(one);

        double a = 3.14;
        System.out.println(a);
        a = 3.15;
        System.out.println(a);
    }
}
